package basic_java;

public class Printing_String_Value {

	public static void main(String[] args) {
		
		//String is a sequence of characters (like a sentence, statement, or word(s))
		//You cannot do any kind of mathematical operations with strings
		
		String name = "Rayhan Rouf";
		
		String age = "29";
		

		System.out.println(name);
		System.out.println(age);
		
		
		System.out.println("My name is "+ name + "  I am "+ age + " years old");

	}

}
